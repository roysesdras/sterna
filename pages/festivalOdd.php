      <div class="container-fluid" data-aos="fade-up">

        <div class="section-title">
          <p style="text-transform: capitalize; font-size: 2em; ">FESTIVAL DES SOLIDARITÉS</p>
          <h1 style="text-align: center; ">LE CONCEPT</h1>
          <img src="../assets/img/concept.jpeg" class="img-fluid rounded mx-auto d-block" style="width: 1300px;" alt="imgSolidarite">
        </div>

            <p class="container" >
              Depuis plus de 20 ans, le Festival des Solidarités (FESTISOL) offre un espace à toutes celles et ceux qui souhaitent montrer les solidarités en action sur leur territoire. <br><br>

              Chaque année en Novembre, des milliers de personnes organisent pendant deux semaines des événements conviviaux et engagés pour parler de solidarité, du local à l’international. 
              Partout, une grande diversité d’acteurs et d’actrices s’engage au quotidien pour faire changer les choses à leur niveau : des associations, des établissements scolaires, des collectivités, des structures socio-culturelles, des entreprises sociales, des citoyen·ne·s, etc. <br><br>

              Animations de rue, pièces de théâtre, jeux de sensibilisation, projections débats, expositions, repas partagés, marchés solidaires, concerts, spectacles de danse, ateliers pratiques... 
              Une diversité d’événements pour toucher tous les publics. <br><br>

              Ce grand temps de mobilisation permet aux acteurs Festisol d’aller à la rencontre des citoyen·ne·s, pour réfléchir ensemble et les inviter à devenir, elles·eux aussi, actrices et acteurs d’un monde plus juste, solidaire et durable. <br><br>

              Les animations ont lieu partout en France, mais aussi dans d'autres pays. Depuis 2015, des collectifs Festisol se sont constitués au Cameroun, au Burkina Faso, au Togo, en Guinée, au Bénin, en République Centrafricaine, en Côte d’Ivoire, au Congo, en RDC, au Niger, au Gabon, au Sénégal, etc. 

            </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2> Pour une solidarité globale</h2>
        </div>
           <p class="container">
              Les membres du Festival des Solidarités entendent promouvoir l’indissociabilité entre les dimensions locale et internationale de la solidarité. <br>
              La solidarité se base d’abord sur une réalité : à l’échelle d’un quartier, d’un village, d’un pays ou du monde, nos vies sont interdépendantes entre elles et avec la nature, et nos actions ont un impact global. Mais la solidarité, c’est pour nous avant tout un choix : c’est décider de voir autrui comme une source d’apprentissage, et comme une force pour transformer ensemble notre monde. 
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>Défendre partout les droits humains et lutter contre les inégalités </h2>
        </div>
           <p class="container">
              À l’heure où les inégalités ne cessent de se creuser, mettre l’accent sur les droits humains est plus que jamais essentiel. Nous énonçons un postulat simple et fort : chaque être humain, où qu’il soit, quel que soit son âge, doit voir garantir l’ensemble de ses droits, tels qu’ils sont définis notamment dans la Déclaration universelle des droits de l’Homme. 
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>Pour une citoyenneté mondiale et solidaire </h2>
        </div>
           <p class="container">
              Porter un regard lucide sur le monde nous amène à cette évidence : la solidarité n’a de sens que si elle est globale. La restreindre à un territoire, à une nationalité, à une culture ou à une religion ne ferait qu’alimenter les conflits et les clivages. Construire la paix implique selon nous de reconnaître le droit légal de chaque être humain à vivre dignement dans un environnement préservé. 
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>Agir ensemble et ne laisser personne de côté </h2>
        </div>
           <p class="container">
              Impliquer l’ensemble des citoyen·ne·s suppose de prendre le temps d’associer chacun·e à la compréhension des enjeux, et de construire et mettre en oeuvre ensemble les solutions, dans une perspective d’émancipation collective. Refusant les approches moralisantes ou culpabilisantes, nos actions de sensibilisation favorisent l’expression d’opinions diverses, et tâchent d’associer les personnes concernées par les sujets abordés. Elles visent à déconstruire nos représentations de « l’autre », près ou loin de nous, et de nous donner à tout·e·s les moyens de comprendre comment fonctionne le monde et comment agir sur lui. Quel que soit le mode d’engagement choisi, 
              celui-ci doit se nourrir de la compréhension globale du monde actuel et des transformations en cours.  
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>Un événement aux nombreux visages</h2>
        </div>
           <p class="container">
              La richesse de cet événement réside dans sa capacité à s’adapter au territoire où il est organisé. Le Festisol est un cadre commun, dont chaque structure ou collectif de structures est libre de s’emparer pour le décliner selon ses thématiques, ses types d’événements et les publics de son choix, dans le cadre d’une charte, garante des valeurs défendues. <br>
              Lutte contre la pauvreté, alimentation, santé, éducation, lutte contre les inégalités, protection du climat et de l'environnement, paix… Ces thématiques sont autant d'enjeux globaux définis par l'Agenda 2030 des Nations unies. Nous portons la vision d'une solidarité globale, aussi bien locale qu'internationale.  
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>Une naissance sous le signe de la solidarité internationale</h2>
        </div>
           <p class="container">
              C’est sous le nom de La Semaine de la solidarité internationale que ce grand temps fort de mobilisation a été lancé en 1998.En 2017, après deux ans de réflexion collective et de concertations, le nom de l’événement est devenu Festival des Solidarités. Cette évolution met en avant la nécessaire continuité entre les échelles locale et internationale, et la volonté de mettre en avant la dimension conviviale et festive des différentes formes de solidarité. 
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>Un festival résolument pédagogique</h2>
        </div>
           <p class="container">
              Au cœur de notre action : l’éducation à la citoyenneté et à la solidarité internationale (ECSI), une démarche pédagogique qui permet de mieux comprendre le monde qui nous entoure, de prendre conscience des interdépendances, et de choisir d’agir pour faire évoluer les sociétés vers plus de solidarité, tout en protégeant l'environnement. Il n’y a pas d’âge pour devenir solidaire.
           </p>

        <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>une solidarité ouverte au monde et aux autres</h2>
        </div>
           <p class="container">
              Notre nourriture, nos vêtements et les produits de notre quotidien proviennent du monde entier. Nos choix de consommation, notre épargne, nos choix politiques, ont un impact bien au-delà de nos frontières. Les migrations, aussi vieilles que l’humanité, sont un exemple parmi d’autres de ce qui nous relie au monde. <br>
              Environnement, finance, alimentation, commerce, santé, pauvreté et démocratie... Ces thématiques sont autant d’enjeux globaux, définis par les Nations unies à travers les Objectifs de développement durable (ODD). Du local à l’international, les droits humains ne sont pas négociables, et la solidarité constitue une réponse à ces défis qui nous concernent tou·te·s. Les alternatives citoyennes sont innombrables. Le Festival des Solidarités est l’occasion de montrer que la solidarité à l’échelle d’un village, d’un quartier, d’un pays, et même du monde, est une réalité déjà à l’œuvre, et que chacun·e peut rejoindre le mouvement.
           </p>

         <div class="section-title" id="section-title" style="padding-top: 2em;">
          <h2>4 bonnes raisons de participer</h2>
        </div>
           <p class="container">
              <b>Se mettre en collectif pour apprendre les uns des autres </b> <br>

                Le travail en collectif est au cœur du Festival des Solidarités depuis ses débuts. En portant des événements à plusieurs structures, vous mutualisez vos ressources, vos compétences et vos moyens d’action, et vous apprenez les un·e·s des autres. Ainsi, vous augmentez vos chances d’attirer de nouveaux publics et vous renforcez l’impact de vos événements. <br><br>

              <b>Créer un lien entre les acteurs d’un territoire</b><br>

                Le Festisol est une occasion pour des acteurs et actrices d’horizons différents de travailler ensemble, pour ensuite tisser des liens de façon plus durable, en retravaillant ensemble à d’autres occasions. Le Festisol fédère depuis plus de 20 ans un large réseau d’actrices et d’acteurs partout en France et dans d’autres pays. Des rencontres locales, régionales et nationales et internationales sont organisées toute l’année pour mieux se connaître, partager nos 
                idées et faire émerger de nouvelles 
                actions communes. <br><br>

                <b>Une visibilité accrue pour votre structure et ses actions</b><br>

                Chaque année, le Festisol organise une grande campagne de communication nationale qui met en avant tous les événements pour permettre aux internautes de trouver les animations Festisol se déroulant près de chez elles·eux. En 2019, ce sont près de 2 000 événements qui étaient répertoriés sur la carte interactive du site du Festival des Solidarités. Vous bénéficiez également de supports de communication gratuits pour vos événements. 
                Pour 2022, le comité international a choisi de mettre en avant la question des jeunesses, de l’interculturalité, du vivre-ensemble et des engagements. Le Festisol est une occasion pour des acteurs et actrices d’horizons différents de travailler ensemble, pour ensuite tisser des liens de façon plus durable, en retravaillant ensemble à d’autres occasions. Le Festisol fédère depuis plus de 20 ans un large réseau d’actrices et d’acteurs partout en France et dans d’autres pays. Des rencontres locales, régionales et nationales sont organisées toute l’année pour mieux se connaître, partager nos idées et faire émerger de nouvelles actions communes. <br><br>

                <b>Un accompagnement tout au long de l’année</b> <br>
                Au-delà de l’événement, le Festisol est aussi un réseau d’acteurs ressources qui peuvent vous accompagner dans vos actions de sensibilisation et d’éducation. Vous bénéficiez ainsi d’outils pédagogiques, de fiches pratiques, de formations et de conseils personnalisés. 
           </p> <br>

