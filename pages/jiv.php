            <p style="text-align:justify">
              Chaque année, l’Assemblée générale des Nations Unies invite les gouvernements à célébrer la Journée internationale des Volontaires pour le développement économique et social le 5 décembre (A / RES / 40/212 du 17 décembre 1985). À la suite de la résolution du 17 décembre 1985, les gouvernements, l’ONU et les organisations de la société civile collaborent avec des bénévoles du monde entier pour célébrer la Journée le 5 décembre de chaque année. <br><br>

              En 2001, année internationale des volontaires, l’Assemblée a adopté une série de recommandations sur les moyens par lesquels les gouvernements et les Nations Unies pourraient soutenir le volontariat et a demandé qu’ils soient largement diffusés. L’Année internationale des volontaires visait à stimuler le débat politique national et international autour de la reconnaissance, la facilitation, le réseautage et la promotion de l’action volontaire. L’année a conduit à une bien meilleure appréciation du pouvoir du volontariat sous ses nombreuses formes et des moyens de le soutenir.<br><br>

              Bénévolat ou volontariat ?<br>
              Pour être tout à fait précis, nous nous devons de dire que la résolution des Nations Unies dont il est fait mention (la 40/212) est plus exactement consacrée au volontariat. Et dans ce cas, on parle volontiers de la Journée Internationale du Volontariat. Mais ne nous leurrons pas, il s'agit bien d'une seule et même journée. La nuance n'existe d'ailleurs qu'en France et son origine est plutôt à rechercher dans les méandres du dictionnaire. <br>
              Ne traduit-on pas <b>"bénévolat" par "Voluntary work" ?</b> <br><br>

              Cette journée vise à sensibiliser les populations et les gouvernements aux contributions volontaires. Il vise également à encourager les gens à offrir leurs services en tant que bénévoles, tant au pays qu’à l’étranger. Au fil des ans, les gouvernements, les entreprises, les organismes sans but lucratif et les particuliers ont contribué à la Journée internationale des bénévoles en participant à diverses activités, notamment: <br>
              <b>–</b> Projets communautaires volontaires. <br>
              <b>–</b> Parades, marches ou rassemblements. <br>
              <b>–</b> Cérémonies de remise de prix pour les bénévoles qui ont apporté une contribution importante à leurs communautés. <br>
              <b>–</b> Campagnes de « don de temps » qui impliquent des personnes engageant des heures de service volontaire pour des projets spécifiques. <br>
              <b>–</b> Les entreprises lancent des programmes volontaires dans le cadre de leur responsabilité d’entreprise.
              <b>–</b> Compétitions bénévoles. <br><br>

              Les activités et les événements de la journée contribuent à promouvoir l’impact du volontariat et les objectifs du Millénaire pour le développement des Nations Unies, en faisant du bénévolat pour:
              <b>–</b> Aidez à éradiquer la pauvreté. <br>
              <b>–</b> Assurer l’éducation primaire universelle. <br>
              <b>–</b> Promouvoir l’égalité des sexes et l’autonomisation des femmes.<br>
              <b>–</b> Réduire la mortalité infantile et améliorer la santé maternelle.<br>
              <b>–</b> Inverser la propagation du VIH / sida, du paludisme et d’autres grandes maladies.<br>
              <b>–</b> Aidez  à assurer la durabilité environnementale.<br>
              Beaucoup de gens participent à beaucoup de ces événements via le World Volunteer Web, que les Volontaires des Nations Unies (VNU) opèrent en partenariat avec diverses organisations. <br>

