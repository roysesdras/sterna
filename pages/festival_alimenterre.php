              Coordonné par le CFSI, le Festival ALIMENTERRE est organisé par plus de 1 000 organisations : lycées, cinémas, associations, collectivités territoriales, fermes, etc. 25 structures nationales sont partenaires de l'évènement. Toute organisation qui souhaite informer et sensibiliser les citoyens des enjeux alimentaires locaux et mondiaux peut organiser un ou plusieurs évènements en partenariat avec d’autres structures de son territoire. Le CFSI propose des outils pédagogiques, des films aux droits négociés, des outils de communication et un accompagnement de proximité réalisé par les coordinateurs territoriaux. 
