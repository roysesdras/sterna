            <p style="text-align:justify">
              Le 20 novembre 1989, l’ONU adoptait à l’unanimité la Convention relative aux droits de l’Enfant: les droits de chaque enfant du monde étaient désormais reconnus par un traité international, ratifié par 195 Etats ! Depuis, le 20 novembre a été déclaré Journée internationale des droits de l’enfant – un temps fort que l’UNICEF ne manque pas de marquer chaque année. <br><br>

              En 1954, l’Assemblée générale a recommandé à tous les pays d’instituer une "Journée mondiale de l’enfance", qui serait une journée de fraternité mondiale et de compréhension entre les enfants, et d’activités favorisant le bien-être des enfants du monde entier. Elle a proposé aux gouvernements que cette Journée soit célébrée à la date qui leur semblait la plus appropriée. C'est le 20 novembre qui a été choisi, à la fois jour d'adoption par l’Assemblée de la Déclaration des droits de l’enfant en 1959, et de la Convention relative aux droits de l’enfant, signée en 1989. <br><br>

              Cette Convention, qui est le traité international le plus ratifié en matière de droits de l'homme, définit une liste de droits de l'enfant comprenant le droit à la vie, à la santé, à l'éducation et le droit de jouer, ainsi que le droit à une vie de famille, à être protégé de la violence et de la discrimination, et de faire entendre sa voix. <br><br>

              Sur la base de cette Convention et des efforts conjoints de l'ensemble des pays et régions, nous soutenons et célébrons chaque année le droit des enfants lors de la Journée mondiale de l'enfance. Par le dialogue et l'action, bâtissons un monde où les enfants peuvent s'épanouir librement. <br><br>

              Chaque année, ce rendez-vous est l'un des plus important dans notre calendrier sur toutes les antennes et nous faisons des enfants :<br>
              * des participants actifs à leur propre vie et à celle de leur collectivité; <br>
              * des citoyens actifs qui peuvent et doivent contribuer de manière significative aux décisions qui   influencent leur vie.<br>
              Cette journée est une excellente occasion pour les enseignants, les éducateurs, les parents et les fournisseurs de soins d'informer les enfants de leurs droits. <br>
              Des échanges autour de leurs doits et devoirs, des ateliers de dessin et de peinture, des jeux et animations, des concours de culture, des théâtres, des contes et récits, etc. <br> Voilà plusieurs activités que nous menons chaque année avec les enfants pour commémorer cette journée si spéciale. <br><br>

